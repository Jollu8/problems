#pragma once

#include "defs.h"

namespace tree {
    /**
     * Возвращает максимальную глубину двоичного дерева.
     *
     * Максимальная глубина — это количество узлов в самом длинном пути
     * от корня дерева до наиболее удаленного листа.
     *
     * @param root Указатель на корень дерева.
     *
     * @return Максимальная глубина дерева.
     *         Если дерево пустое, возвращает 0.
     *
     * Сложность:
     * - Время: O(n), где n — количество узлов.
     * - Память: O(h), где h — высота дерева, из-за рекурсии.
     */
    int maxDepth(utils::TreeNode *root);

    /**
     * Разворачивает односвязный список в обратном направлении.
     *
     * После выполнения функции все указатели next должны быть изменены так,
     * чтобы порядок элементов стал противоположным.
     *
     * Пример:
     * 1 -> 2 -> 3 -> nullptr
     *
     * После разворота:
     * 3 -> 2 -> 1 -> nullptr
     *
     * @param head Указатель на первый элемент списка.
     *
     * @return Указатель на новую голову списка.
     *         Если список пустой, возвращается nullptr.
     *
     * Сложность:
     * - Время: O(n)
     * - Память: O(1)
     */
    utils::Node *reverseList(utils::Node *head);
}
